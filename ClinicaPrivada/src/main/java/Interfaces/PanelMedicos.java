    public PanelMedicos() {
        initComponents();
        
        conexionEspecialidad = new EspecialidadDAO(); // son nuevas conexiones a la base de datos
        conexionMedico = new MedicoDAO();
        
        modeloMedico = (DefaultTableModel) tabla.getModel(); //voy a hacer que modeloMedico(tabla) sea del mismo formato que nuestra tabla de medicos
        
        Object datos[] = new Object[5];//voy a crear los objetos que entraran a la tabla.. que son 5
        //Se crea como object.. ya que.. entraran enteros.. strings.. fechas..
        
        modeloMedico.setRowCount(0);//inicializo el conteo de la tabla en 0
        
        for (Medico medico : conexionMedico.mostrar()) {//conexionMedico.mostrar me dara una lista de todos los medicos registrados en la base de datos
            datos[0] = medico.getDni();//
            datos[1] = medico.getNombre();
            datos[2] = medico.getApellido();
            datos[3] = medico.getFechaNacimiento();
            datos[4] = conexionEspecialidad.obtenerEspecialidadByID(medico.getId_especialidad()).getNombre();
            modeloMedico.addRow(datos);//con los objetos que hemos creado, hemos creado una fila. y esta se pasara a la tabla 
        }
        
        tEspecialidad.removeAllItems();//aca limpio el combo box de especialidad
        for(Especialidad especialidad : conexionEspecialidad.mostrar()){// obtengo todas las especialidades
            tEspecialidad.addItem(especialidad.getNombre());//asignar una especialidad 
        }
    }

