    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String nombre,apellido,dni;
        Date fecha_nacimiento;
        
        nombre = tNombre.getText();
        apellido = tApellido.getText();
        dni = tDni.getText();
        
        int esp = tEspecialidad.getSelectedIndex() + 1; // aumentamos en uno ya que la lista del combobox empieza en 0, debemos chapar su id para aumentar en 1
        
        java.util.Date selectedDate = tNacimiento.getDate(); //oye formateame la fecha que me ha pasado el usuario a una fecha de tipo util.Date
        
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");//oye ahora formateame en el formateo YYYY-MM-DD
        String fecha_string = dateFormat.format(selectedDate); //Fecha obtenida en formato yyyy-mm-dd y esta se pasara a string "yyyy-mm-dd";
            
        fecha_nacimiento = Date.valueOf(fecha_string);//ahora de la fecha obtenida del usuario se la pasamos al tipo de dato DATE de variable fecha_nacimiento
        
        conexionMedico.insertar(new Medico(nombre,apellido,dni,fecha_nacimiento,esp)); //AQUI YA HA INSERTADO EL NUEVO MEDICO A LA TABLA
        
        //LO HA INSERTADO.. PERO AUN LA TABLA NO SE HA ACTUALIZADO
               
        //ACTUALIZANDO TABLA
        modeloMedico = (DefaultTableModel) tabla.getModel(); // LIMPIANDO LA TABLA
        
        Object datos[] = new Object[5];//CREANDO MIS OBJETOS
        
        modeloMedico.setRowCount(0);//RESETEANDO A 0 EL CONTEO DE LA TABLA
        
        for (Medico medico : conexionMedico.mostrar()) {//NUEVAMENTE PASANDOLE TODOS LOS DATOS
            datos[0] = medico.getDni();
            datos[1] = medico.getNombre();
            datos[2] = medico.getApellido();
            datos[3] = medico.getFechaNacimiento();
            datos[4] = conexionEspecialidad.obtenerEspecialidadByID(medico.getId_especialidad()).getNombre();
            modeloMedico.addRow(datos);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

